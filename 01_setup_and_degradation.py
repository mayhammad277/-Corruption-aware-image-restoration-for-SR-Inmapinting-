for col, label in enumerate(col_labels):
    axes[0][col].set_title(label, fontsize=11, fontweight='bold', pad=8)

for row, path in enumerate(sample_paths):
    hr  = load_and_resize(path)
    lr  = make_lr(hr)
    lr_up = lr.resize((IMG_SIZE, IMG_SIZE), Image.BICUBIC)

    hr_np  = np.array(hr) / 255.0
    mask   = make_rect_mask(IMG_SIZE, IMG_SIZE)
    masked = apply_mask(hr_np, mask)

    mask_vis = np.stack([mask]*3, axis=-1)  # greyscale mask as RGB

    for col, img in enumerate([hr_np, np.array(lr)/255.0, np.array(lr_up)/255.0, mask_vis, masked]):
        axes[row][col].imshow(img if img.shape[-1]==3 else img, cmap='gray')
