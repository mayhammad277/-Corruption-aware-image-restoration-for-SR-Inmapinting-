"""## 💾 Step 5 — Run Full Degradation Pipeline & Save to Drive"""

from tqdm import tqdm

all_paths = sorted([os.path.join(SPEEDPLUS_DIR, f)
                    for f in os.listdir(SPEEDPLUS_DIR)
                    if f.endswith(('.jpg', '.png'))])

if MAX_IMAGES:
    all_paths = all_paths[:MAX_IMAGES]

print(f'Processing {len(all_paths)} images...')

for path in tqdm(all_paths, desc='Degradation pipeline'):
    stem = os.path.splitext(os.path.basename(path))[0]

    # ── Load & resize ────────────────────────────────────────────────────────
    hr  = load_and_resize(path, IMG_SIZE)
    hr_np = np.array(hr, dtype=np.float32) / 255.0

    # ── Super-Resolution pair ────────────────────────────────────────────────
    lr = make_lr(hr, SR_SCALE)
    hr.save(f'{SR_HR_DIR}/{stem}.png')
    lr.save(f'{SR_LR_DIR}/{stem}.png')

    # ── Inpainting pair (mixed: 50% rect, 50% irregular) ─────────────────────
    if random.random() > 0.5:
        mask = make_rect_mask(IMG_SIZE, IMG_SIZE)
    else:
        mask = make_irregular_mask(IMG_SIZE, IMG_SIZE)

    masked = apply_mask(hr_np, mask)

    hr.save(f'{INP_GT_DIR}/{stem}.png')
    Image.fromarray((mask * 255).astype(np.uint8)).save(f'{INP_MASK_DIR}/{stem}.png')
    Image.fromarray((masked * 255).astype(np.uint8)).save(f'{INP_MASKED_DIR}/{stem}.png')

print('\n✅ Degradation complete!')
print(f'   SR pairs  → {SR_LR_DIR}')
print(f'   Inpainting → {INP_GT_DIR}')

