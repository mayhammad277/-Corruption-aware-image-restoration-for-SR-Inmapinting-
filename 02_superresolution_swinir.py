"""## 📋 Step 9 — Full Quantitative Evaluation Table"""

import math, csv
from skimage.metrics import structural_similarity as skssim

model.eval()
results = []

with torch.no_grad():
    for lr_t, hr_t, name in val_loader:
        pred = run_swinir(model, lr_t.to(DEVICE)).cpu().clamp(0, 1)
        # Bicubic baseline
        bicubic = torch.nn.functional.interpolate(
            lr_t, size=(IMG_SIZE, IMG_SIZE), mode='bicubic', align_corners=False).clamp(0, 1)

        pred_np    = pred[0].permute(1,2,0).numpy()
        hr_np      = hr_t[0].permute(1,2,0).numpy()
        bicubic_np = bicubic[0].permute(1,2,0).numpy()

        mse_pred = np.mean((pred_np - hr_np)**2)
        mse_bic  = np.mean((bicubic_np - hr_np)**2)

        results.append({
            'name':          name[0],
            'psnr_bicubic':  10*math.log10(1/mse_bic)  if mse_bic > 1e-10 else 100,
            'psnr_swinir':   10*math.log10(1/mse_pred)  if mse_pred > 1e-10 else 100,
            'ssim_bicubic':  skssim(bicubic_np, hr_np, data_range=1.0, channel_axis=2),
            'ssim_swinir':   skssim(pred_np,    hr_np, data_range=1.0, channel_axis=2),
        })

avg = {k: np.mean([r[k] for r in results]) for k in results[0] if k != 'name'}

