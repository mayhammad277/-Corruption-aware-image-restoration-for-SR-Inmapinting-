class SRDataset(Dataset):
    def __init__(self, lr_dir, hr_dir, augment=True):
        self.lr_paths = sorted([os.path.join(lr_dir, f)
                                 for f in os.listdir(lr_dir)
                                 if f.endswith(('.png', '.jpg'))])
        self.hr_dir  = hr_dir
        self.augment = augment

    def __len__(self):
        return len(self.lr_paths)

    def __getitem__(self, idx):
        lr_path = self.lr_paths[idx]
        hr_path = os.path.join(self.hr_dir, os.path.basename(lr_path))

        lr = Image.open(lr_path).convert('RGB')
        hr = Image.open(hr_path).convert('RGB')

        # Augmentation
        if self.augment:
            if random.random() > 0.5:
                lr, hr = TF.hflip(lr), TF.hflip(hr)
            if random.random() > 0.5:
                lr, hr = TF.vflip(lr), TF.vflip(hr)

        lr_t = TF.to_tensor(lr)  # [3, H/4, W/4]
        hr_t = TF.to_tensor(hr)  # [3, H, W]
        return lr_t, hr_t, os.path.splitext(os.path.basename(lr_path))[0]


