#  pad input to multiple of window_size, then crop
def pad_to_window(x, window_size=WINDOW_SIZE):
    """Pad spatial dims so H and W are multiples of window_size."""
    _, _, h, w = x.shape
    pad_h = (window_size - h % window_size) % window_size
    pad_w = (window_size - w % window_size) % window_size
    return F.pad(x, (0, pad_w, 0, pad_h), mode='reflect'), h, w

def run_swinir(model, lr_batch, scale=SR_SCALE):
    """Run SwinIR with automatic padding/cropping for any input size."""
    x, h, w = pad_to_window(lr_batch)
    out = model(x)
    return out[:, :, :h*scale, :w*scale]   # crop to original HR size

print('✅ run_swinir() helper defined — handles any input resolution')

"""## 🏋️ Step 6 — Training Loop"""

import math
import time
import torch.optim as optim
from torch.optim.lr_scheduler import CosineAnnealingLR

#  Hyperparameters
NUM_EPOCHS = 100
LR         = 2e-4
PATIENCE   = 15     # Early-stopping patience (epochs)
SAVE_EVERY = 10
#

criterion = nn.L1Loss()
optimizer = optim.Adam(model.parameters(), lr=LR, betas=(0.9, 0.999))
scheduler = CosineAnnealingLR(optimizer, T_max=NUM_EPOCHS, eta_min=1e-7)


def calc_psnr(pred, gt, max_val=1.0):
    mse = torch.mean((pred - gt) ** 2).item()
    return 10 * math.log10(max_val**2 / mse) if mse > 1e-10 else float('inf')


def validate(model, loader):
    model.eval()
    total_psnr, total_loss = 0, 0
    with torch.no_grad():
        for lr_t, hr_t, _ in loader:
            lr_t, hr_t = lr_t.to(DEVICE), hr_t.to(DEVICE)
            pred = run_swinir(model, lr_t)
            pred = torch.clamp(pred, 0, 1)
            total_loss += criterion(pred, hr_t).item()
            total_psnr += calc_psnr(pred, hr_t)
    n = len(loader)
    return total_loss / n, total_psnr / n


history = {'train_loss': [], 'val_loss': [], 'val_psnr': []}
best_psnr   = 0
no_improve  = 0

print(f'Starting training for up to {NUM_EPOCHS} epochs...')
print(f'{"Epoch":>6}  {"Train Loss":>10}  {"Val Loss":>9}  {"Val PSNR":>9}  {"LR":>10}  {"Time":>7}')
print('-' * 65)

for epoch in range(1, NUM_EPOCHS + 1):
    model.train()
    t0 = time.time()
    train_loss = 0

    for lr_t, hr_t, _ in train_loader:
        lr_t, hr_t = lr_t.to(DEVICE), hr_t.to(DEVICE)
        optimizer.zero_grad()
        pred = run_swinir(model, lr_t)
        loss = criterion(pred, hr_t)
        loss.backward()
        optimizer.step()
        train_loss += loss.item()

    train_loss /= len(train_loader)
    val_loss, val_psnr = validate(model, val_loader)
    scheduler.step()

    history['train_loss'].append(train_loss)
    history['val_loss'].append(val_loss)
    history['val_psnr'].append(val_psnr)

    elapsed = time.time() - t0
    lr_now  = scheduler.get_last_lr()[0]
    print(f'{epoch:>6}  {train_loss:>10.4f}  {val_loss:>9.4f}  {val_psnr:>9.2f}  {lr_now:>10.2e}  {elapsed:>6.1f}s')

    # Save best
    if val_psnr > best_psnr:
        best_psnr  = val_psnr
        no_improve = 0
        torch.save({'epoch': epoch, 'state_dict': model.state_dict(),
                    'psnr': best_psnr, 'optimizer': optimizer.state_dict()},
                   f'{CKPT_DIR}/swinir_best.pth')
        print(f'  💾 Best model saved  (PSNR {best_psnr:.2f} dB)')
    else:
        no_improve += 1

    # Periodic checkpoint
    if epoch % SAVE_EVERY == 0:
        torch.save({'epoch': epoch, 'state_dict': model.state_dict()},
                   f'{CKPT_DIR}/swinir_epoch{epoch}.pth')

    # Early stopping
    if no_improve >= PATIENCE:
        print(f'\n⏹  Early stopping at epoch {epoch} (no improvement for {PATIENCE} epochs)')
        break

print(f'\n✅ Training complete. Best Val PSNR: {best_psnr:.2f} dB')

